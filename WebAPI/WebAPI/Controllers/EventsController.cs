        public JsonResult Put(Events ev)
        {
            string query = @"
                    update Events set 
                    EventName = '" + ev.EventName + @"',
                    EventDescription = '" + ev.EventDescription + @"',
                    EventStartDate = '" + ev.EventStartDate + @"',
                    EventEndDate = '" + ev.EventEndDate + @"',
                    Status = '" + ev.Status + @"'
